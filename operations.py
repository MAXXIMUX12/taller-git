def add(num_1, num_2):
  result = num_1+num_2
  print(num_1 ,"+", num_2," is equal to", result)
  return result
  
def subtract(num_1, num_2):
  result=num_1-num_2
  print(num_1,"-",num_2,"is equal to",result)
  return result
  
def multiplication(num_1, num_2):
  result=num_1*num_2
  print(num_1,"*",num_2,"is equal to",result)
  return result
  
def division(num_1, num_2):
  result=num_1//num_2
  print(num_1,"/",num_2,"is equal to",result)
  return result
  
def power(num_1, num_2):
  result=num_1**num_2
  print(num_1,"power",num_2,"is equal to",result)
  return result
  
def module(num_1, num_2):
  result=num_1%num_2
  print(num_1,"%",num_2,"is equal to",result)
  return result
